class Solution
{
    public:
    //Function to return the lowest common ancestor in a Binary Tree.
    pair<bool,bool> helperFunc(Node* root,int p,int q,Node* &LCA){
        if(root==NULL)
            return {false,false};
        pair<bool,bool> leftSol = helperFunc(root->left,p,q,LCA);
        pair<bool,bool> rightSol = helperFunc(root->right,p,q,LCA);
        pair<bool,bool> rootCheck = {leftSol.first||rightSol.first||root->data==p,leftSol.second||rightSol.second||root->data==q};
        if(rootCheck.first&&rootCheck .second&&LCA ==NULL)
            LCA = root;
        return rootCheck;
              
        
    }
    Node* lca(Node* root ,int p ,int q )
    {
       //Your code here 
       if(!root )
            return NULL;
        Node* LCA=NULL;
        pair<bool,bool> sol = helperFunc(root,p,q,LCA);
        if(sol.first && sol.second)
            return LCA;
        else 
            return NULL;
    }
};

