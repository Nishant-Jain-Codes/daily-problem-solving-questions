class Solution{
  public:
    // Function to find floor of x
    // n: size of vector
    // x: element whose floor is to find
    int findFloor(vector<long long> v, long long n, long long x){
        
        // Your code here
        int ans = -1;
        int si=0,ei=n-1,mid;
        while(si<=ei){
            mid = si + ((ei-si)/2);
            if(v[mid]>x){
                ei=mid-1;
            }else if(v[mid]<x){
                ans = mid;
                si=mid+1;
            }
            else 
                return mid;
        }
        return ans;
    }
};


