class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int target)
    {
        // code here
        Node * front = head;
        Node* tail = head;
        while(tail->next!=NULL)
            tail = tail->next;
        vector<pair<int,int>> ans;
        while(front != NULL && tail!= front){
            if((front->data + tail->data) > target){
                tail=tail->prev;
            }
            else if((front->data + tail->data) < target){
                front = front->next;
            }
            else {
                ans.push_back({front->data,tail->data});
                //if(front!=tail)
                    //ans.push_back({tail->data,front->data});
                front = front -> next;
            }
        
        }
        return ans;
    }
};

